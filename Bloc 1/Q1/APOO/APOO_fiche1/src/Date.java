class Date {

    private int jour, mois, annee;

    Date( int jour, int mois, int annee){

        this.jour = jour;
        this.mois = mois;
        this.annee = annee;

    }

    public int getJour(){

        return this.jour;

    }

    public int getMois(){

        return this.mois;

    }

    public int getAnnee(){

        return this.annee;

    }

}
